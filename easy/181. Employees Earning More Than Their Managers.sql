-- Input: 
-- Employee table:
-- +----+-------+--------+-----------+
-- | id | name  | salary | managerId |
-- +----+-------+--------+-----------+
-- | 1  | Joe   | 70000  | 3         |
-- | 2  | Henry | 80000  | 4         |
-- | 3  | Sam   | 60000  | Null      |
-- | 4  | Max   | 90000  | Null      |
-- +----+-------+--------+-----------+
-- Output: 
-- +----------+
-- | Employee |
-- +----------+
-- | Joe      |
-- +----------+

-- # Write your MySQL query statement below

SELECT e1.name AS `Employee`
FROM
    Employee AS `e1`,
    Employee AS `e2`
WHERE
    e1.managerId = e2.id
    AND e1.salary > e2.salary;

